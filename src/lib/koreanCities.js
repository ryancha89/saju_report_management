/**
 * 생년월일과 경도를 기반으로 시간 조정값 계산
 */
export const calculateTimeAdjustment = (birthDate, longitude) => {
  const [year, month, day] = birthDate.split('-').map(Number);

  // 생년월일에 따른 기준 경도 결정
  let long_base;
  if (year < 1912 || (year === 1912 && month === 1 && day < 1)) {
    long_base = 135;  // 일본 표준시
  } else if ((year > 1954 || (year === 1954 && month > 3) || (year === 1954 && month === 3 && day >= 21)) &&
            (year < 1961 || (year === 1961 && month < 8) || (year === 1961 && month === 8 && day <= 9))) {
    long_base = 127.5;  // 한국 표준시 (동경 127.5도)
  } else if (year > 1961 || (year === 1961 && month > 8) || (year === 1961 && month === 8 && day >= 10)) {
    long_base = 135;  // 다시 동경 135도 기준
  } else {
    long_base = 135;  // 기본값
  }

  // 시간 차이 계산
  const longDifference = (long_base - longitude) * -1;
  let timeDifferenceInMinutes = longDifference * 4;  // 경도 1도 = 4분

  const negative = timeDifferenceInMinutes < 0;
  if (negative) {
    timeDifferenceInMinutes = timeDifferenceInMinutes * -1;
  }

  const minutes = Math.floor(timeDifferenceInMinutes);
  const seconds = Math.floor((timeDifferenceInMinutes - minutes) * 60);

  return {
    timeAdjustment: `${negative ? '-' : '+'}${minutes}/${seconds}`,
    minutes: negative ? -minutes : minutes,
    seconds,
    displayText: `${negative ? '-' : '+'}${minutes}분${seconds}초`
  };
};
